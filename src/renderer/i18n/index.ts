/** Pure function — usable outside React (e.g. in tests) */
export function translate(language: string, key: string): string {
  return translations[language as Lang]?.[key] ?? translations['zh-CN'][key] ?? key;
}

export function useTranslation() {
  const language = useConfigStore((s) => s.language);
  const t = (key: string): string => translate(language, key);
  return { t, language };
}
